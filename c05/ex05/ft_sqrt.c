int	ft_sqrt(int nb)
{
	int	x0;
	int	x1;

	if (nb <= 1)
		return (0);
	x0 = nb / 2;
	x1 = (x0 + nb / x0) / 2;
	while (x1 < x0)
	{
		x0 = x1;
		x1 = (x0 + nb / x0) / 2;
	}
	if (x0 * x0 == nb)
	{
		return (x0);
	}
	else
	{
		return (0);
	}
	return (x0);
}
